package com.pakt.pojos;

public class device_count {
	String companyname;
	String availdevices;
	String solddevices;
	String totaldevices;
	
	
	public String getCompanyname() {
		return companyname;
	}
	public void setCompanyname(String companyname) {
		this.companyname = companyname;
	}
	public String getAvaildevices() {
		return availdevices;
	}
	public void setAvaildevices(String availdevices) {
		this.availdevices = availdevices;
	}
	public String getSolddevices() {
		return solddevices;
	}
	public void setSolddevices(String solddevices) {
		this.solddevices = solddevices;
	}
	public String getTotaldevices() {
		return totaldevices;
	}
	public void setTotaldevices(String totaldevices) {
		this.totaldevices = totaldevices;
	}
	
	
}
